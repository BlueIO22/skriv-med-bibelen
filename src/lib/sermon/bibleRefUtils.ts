export const BIBLE_REF_RE =
  /\b(?:[1-3]\.\s+|[1-3]\s+)?(?:Mos(?:ebok)?|Jos(?:ua)?|Dom(?:merne)?|Rut|Sam(?:uel)?|Kong(?:ene)?|Krøn(?:ikene)?|Esra|Neh(?:emia)?|Est(?:er)?|Job|Sal(?:menes?)?|Ord(?:språkene)?|Fork(?:ynnerens?)?|Høys(?:angen)?|Jes(?:aja)?|Jer(?:emia)?|Klag(?:esangene)?|Esek(?:iel)?|Dan(?:iel)?|Hos(?:ea)?|Joel|Am(?:os)?|Ob(?:adja)?|Jon(?:a)?|Mik(?:a)?|Nah(?:um)?|Hab(?:akkuk)?|Sef(?:anja)?|Hag(?:gai)?|Sak(?:arja)?|Mal(?:aki)?|Matt(?:eus)?|Mark(?:us)?|Luk(?:as)?|Joh(?:annes)?|Apg|Rom(?:erne)?|Kor(?:inter(?:brevet)?)?|Gal(?:aterne)?|Ef(?:eserne)?|Fil(?:ipperne)?|Kol(?:osserne)?|Tess(?:alonikerne)?|Tim(?:oteus)?|Tit(?:us)?|Filem(?:on)?|Hebr(?:eerne)?|Jak(?:ob)?|Pet(?:er)?|Jud(?:as)?|Åp(?:enbaring(?:en)?)?)\b\.?\s+\d+(?::\d+(?:[–\-]\d+)?)?/g;

export function extractBibleRefs(text: string): string[] {
  BIBLE_REF_RE.lastIndex = 0;
  const matches = text.match(BIBLE_REF_RE);
  if (!matches) return [];
  return [...new Set(matches.map((m) => m.trim()))];
}
